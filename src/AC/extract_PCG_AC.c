#include "fd.h"

void extract_PCG_AC(float * PCG_old, float ** waveconv){

	extern int NX, NY, IDX, IDY;
	int i, j, h;
	
	/* ============================================================================================================================================================== */
	/* ===================================================== GRADIENT Vp ============================================================================================ */
	/* ============================================================================================================================================================== */

        h=1;
	/* store gradient */
	for (i=1;i<=NX;i=i+IDX){
	   for (j=1;j<=NY;j=j+IDY){

		 waveconv[j][i] = PCG_old[h];

                 h++;
	   }
	}

}
